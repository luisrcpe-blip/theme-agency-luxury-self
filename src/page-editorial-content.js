export const EDITABLE_PAGE_KEYS = Object.freeze(["interiors", "services", "about", "contact"]);

export function buildEditablePageSeed(page, locale) {
  const service = servicePageContent[page];
  if (service) {
    const principles = service.principles[locale];
    if (!service.title[locale] || !service.lead[locale] || !service.eyebrow[locale] || !principles) {
      throw new Error(`Falta contenido editorial compartido para ${page}:${locale}.`);
    }
    return {
      title: service.title[locale],
      excerpt: service.lead[locale],
      content: [
        `## ${service.eyebrow[locale]}`,
        service.lead[locale],
        ...principles.flatMap((principle) => [`### ${principle.title}`, principle.body]),
      ].join("\n\n"),
    };
  }

  if (page === "contact") {
    const contact = contactPageContent[locale];
    if (!contact) throw new Error(`Falta contenido editorial compartido para contact:${locale}.`);
    return {
      title: contact.contactHeroTitle,
      excerpt: contact.contactHeroLead,
      content: [
        `## ${contact.contactHeroEyebrow}`,
        contact.contactHeroLead,
        `### ${contact.locationTitle}`,
        contact.locationValue,
        `### ${contact.callUs}`,
        CONTACT_DETAILS.phone,
        `### ${contact.writeUs}`,
        CONTACT_DETAILS.email,
        `> ${contact.contactQuote}`,
        `### ${contact.contactFormTitle}`,
        `_${contact.contactFormEyebrow}_`,
        contact.contactFormLead,
        `### ${contact.areaLabel}`,
        contact.locationValue,
      ].join("\n\n"),
    };
  }

  return null;
}
